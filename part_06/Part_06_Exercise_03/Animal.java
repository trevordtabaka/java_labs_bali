package part_06.Part_06_Exercise_03;

public class Animal {

    private int eyes;
    private int limbs;

    public Animal(int eyes, int limbs) {
        this.eyes = eyes;
        this.limbs = limbs;
    }

    public int getEyes() {
        return eyes;
    }

    public void setEyes(int eyes) {
        this.eyes = eyes;
    }

    public int getLimbs() {
        return limbs;
    }

    public void setLimbs(int limbs) {
        this.limbs = this.limbs;
    }
}
